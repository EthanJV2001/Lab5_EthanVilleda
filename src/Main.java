    private void jb_CarreraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_CarreraMouseClicked
        nombreEst=tf_NombreEst.getText();
        apellidoEst=tf_ApellidoEst.getText();
        numcuenta=Integer.parseInt(tf_NumeroCuenta.getText());
        edadEst=(int)js_EdadEst.getValue();
        sexo=cb_SexoEst.getSelectedIndex();
        facultad=cb_FacultadEst.getSelectedIndex();
        switch(facultad){
            case 0: 
                jd_AgEst.setVisible(false);
                jd_Licenciaturas.pack();
                jd_Licenciaturas.setModal(true);
                jd_Licenciaturas.setVisible(true);
                break;
            case 1: 
                jd_AgEst.setVisible(false);
                jd_Ingenierias.pack();
                jd_Ingenierias.setModal(true);
                jd_Ingenierias.setVisible(true);
                break;
            case 2: 
                jd_AgEst.setVisible(false);
                jd_Ciencias.pack();
                jd_Ciencias.setModal(true);
                jd_Ciencias.setVisible(true);
                break;
        }
    }//GEN-LAST:event_jb_CarreraMouseClicked

    private void jb_AgLicMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_AgLicMouseClicked
        ArrayList<Estudiante> estudiantes=new ArrayList();
        switch(cb_CarreraLic.getSelectedIndex()){
            case 0: nombre_Carrera="Finanzas";
                break;
            case 1: nombre_Carrera="Derecho";
                break;
            case 2: nombre_Carrera="Administracion de Empresas";
                break;
            case 3: nombre_Carrera="Dise침o Grafico";
                break;
            case 4: nombre_Carrera="Mercadotecnia";
                break;
        }
        estudiantes.add(new Estudiante(nombreEst, apellidoEst, numcuenta, edadEst, sexo, facultad, new Carrera(facultad, nombre_Carrera, Double.parseDouble(tf_CostoLic.getText()), tf_JefeLic.getText())));
    }//GEN-LAST:event_jb_AgLicMouseClicked

